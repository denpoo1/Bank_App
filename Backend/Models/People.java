package Models;

public class People {
    private int age;
    private int name;
    private int surname;

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getName() {
        return name;
    }

    public void setName(int name) {
        this.name = name;
    }

    public int getSurname() {
        return surname;
    }

    public void setSurname(int surname) {
        this.surname = surname;
    }

    public People(int age, int name, int surname) {
        this.age = age;
        this.name = name;
        this.surname = surname;
    }
//Тестирование Джира 
    // Второй коммит 
}
